package com.newbrain.model;

public class Model_CarManager {

	private String deviceNum;
	private String carNum;
	private String carVIN;
	private String carSort;
	private String carBuyTime;
	private String carRecommended;

	public String getDeviceNum() {
		return deviceNum;
	}

	public void setDeviceNum(String deviceNum) {
		this.deviceNum = deviceNum;
	}

	public String getCarNum() {
		return carNum;
	}

	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}

	public String getCarVIN() {
		return carVIN;
	}

	public void setCarVIN(String carVIN) {
		this.carVIN = carVIN;
	}

	public String getCarSort() {
		return carSort;
	}

	public void setCarSort(String carSort) {
		this.carSort = carSort;
	}

	public String getCarBuyTime() {
		return carBuyTime;
	}

	public void setCarBuyTime(String carBuyTime) {
		this.carBuyTime = carBuyTime;
	}

	public String getCarRecommended() {
		return carRecommended;
	}

	public void setCarRecommended(String carRecommended) {
		this.carRecommended = carRecommended;
	}

}
